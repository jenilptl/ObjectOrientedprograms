public class OperatorPrecedence {
    public static void main(String[] args) {
        System.out.println("a. 10 + 20 * 30 = " + (10 + 20 * 30));
        System.out.println("b. 100 / 10 * 100 = " + (100 / 10 * 100));
        System.out.println("c. 5 * 4 / 4 % 3 = " + (5 * 4 / 4 % 3));
        System.out.println("d. 100 + 200 / 10 - 3 * 10 = " + (100 + 200 / 10 - 3 * 10));
    }
}
